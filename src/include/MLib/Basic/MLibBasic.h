/** アライメント計算マクロ */
#define MLIB_BASIC_ALIGN( _VALUE, _ALIGNMENT )                              \
    ( ( ( _VALUE ) + ( ( _ALIGNMENT ) - 1 ) ) & ~( ( _ALIGNMENT ) - 1 ) )

/** フラグ判定マクロ */
#define MLIB_BASIC_HAVE_FLAG( _VALUE, _FLAG )   \
    ( ( ( _VALUE ) & ( _FLAG ) ) == ( _FLAG ) )


