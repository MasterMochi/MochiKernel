/**
 * @brief       割込み管理初期化
 * @details     割込み管理内サブモジュールの初期化を行う。
 */
/******************************************************************************/
void IntMngInit( void )
{
    /* IDT管理サブモジュール初期化 */
    IntMngIdtInit();
    
    /* ハンドラ管理サブモジュール初期化 */
    IntMngHdlInit();
    
    /* PIC管理サブモジュール初期化 */
    IntMngPicInit();
    
    return;
}


