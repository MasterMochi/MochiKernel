/**
 * @brief       メモリ管理初期化
 * @details     GDT管理サブモジュールの初期化を呼び出し、メモリ管理機能を初期化
 *              する。
 * 
 * @param[in]   *pMap メモリマップ
 * @param[in]   size  メモリマップサイズ
 */
/******************************************************************************/
void MemMngInit( MochiKernelMemoryMap_t *pMap,
                 size_t                 mapSize )
{
    /* GDT管理サブモジュール初期化 */
    MemMngGdtInit();
    
    /* メモリ領域管理サブモジュール初期化 */
    MemMngAreaInit( pMap, mapSize );
    
    return;
}


